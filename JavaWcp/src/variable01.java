
public class variable01 {
	public static void main(String[] args) {
		//int型の変数num1を宣言
		int num1;
		//String型の変数str1を宣言
		String str1;
		//num1を初期化
		num1 = 10;
		//str1を初期化
		str1 = "Hello World!!";
		System.out.println(num1);
		System.out.println(str1);

		//num1に20を再代入
		num1 = 20;
		System.out.println(num1);

		//int型の変数num2を宣言と同時に初期化
		int num2 = 30;
		System.out.println(num2);
	}
}