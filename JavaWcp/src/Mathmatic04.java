//関係(比較)演算子
public class Mathmatic04 {
	public static void main(String[] Args) {
		int a = 5;
		boolean c;
		
		c = a < 5;
		System.out.println(c);
		c = a <= 5;
		System.out.println(c);
		c = a > 4;
		System.out.println(c);
		c = a >= 6;
		System.out.println(c);
		c = a == 5;
		System.out.println(c);
		c = a != 5;
		System.out.println(c);
	}
}
