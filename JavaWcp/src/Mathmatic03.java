//代入演算子
public class Mathmatic03 {
	public static void main(String[] args) {
		int a;
		// a に 10 を代入
		a = 10;
		System.out.println(a);
		// a に a+5 の結果を代入
		a += 5;
		System.out.println(a);
		// a に a-3 の結果を代入
		a -= 3;
		System.out.println(a);
		// a に a*4 の結果を代入
		a *= 4;
		System.out.println(a);
		// a に a/2 の結果を代入
		a /= 2;
		System.out.println(a);
		// a に　a%7 の結果を代入
		a %= 7;
		System.out.println(a);
	}
}
